# exercise_01.rb

## Problem
# What does the each method in the following program return after it is finished executing?
#
# x = [1, 2, 3, 4, 5]
# x.each do |a|
#   a + 1
# end

## Solution
# - According to the Ruby documentation, the Array#each method returns the original array
#   that called on the method
# - In this case, the method will return the array [1, 2, 3, 4, 5] 
