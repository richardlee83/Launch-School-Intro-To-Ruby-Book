## Problem
# Given the following expression, how would you access the name of the person?
#
# person = {name: 'Bob', occupation: 'web developer', hobbies: 'painting'}

## Solution
person = {name: 'Bob', occupation: 'web developer', hobbies: 'painting'}

# access name of person
person[:name]     # Return value: "Bob"
