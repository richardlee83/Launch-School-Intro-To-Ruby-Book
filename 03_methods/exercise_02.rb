## Question
# What do the following expressions evaluate to?
#
#   1. x = 2
#
#   2. puts x = 2
#
#   3. p name = "Joe"
#
#   4. four = "four"
#
#   5. print something = "nothing"

## Answer
#
#   1. x = 2                            # 2
#
#   2. puts x = 2                       # nil
#
#   3. p name = "Joe"                   # "Joe"
#
#   4. four = "four"                    # "four"
#
#   5. print something = "nothing"      # nil
